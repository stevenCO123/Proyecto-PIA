                <h1>AUDM (Administración Universal De Materiales)</h1>
                <p>
                    es un proyecto que busca crear una página web con la cual no
                    solo la Institución Educativa Lola Gonzales sino los
                    profesores puedan acceder al inventario completo de
                    materiales tanto móviles e inmóviles dentro de los salones
                    de clase y el resto de instalaciones pertenecientes a la
                    institución (coordinación, laboratorio, biblioteca,
                    gimnasio, tienda, papelería, enfermería, restaurante, etc…)
                    en busca de saber que falta o saber con qué cuentan desde un
                    inicio.
                </p>
                <br />
                <h1>Problema o necesidad.</h1>
                <p>
                    La institución tiene el problema de que carece de materiales
                    para remplazar en las instalaciones sin conocer de manera
                    certera que deben remplazar (lámparas, sillas, mesas,
                    marcadores, borradores, basuras, libros, computadores,
                    materiales del laboratorio etc…) causando que la integridad
                    de la escuela se vea afectada de manera drástica y la
                    calidad de la institución se perjudique tanto académicamente
                    como disciplinariamente
                </p>
                <br />
                <h1>Marco teórico y estado del arte</h1>
                <p>
