                    fundamentos, etc.). Descripción de tecnologías y desarrollos
                    similares tomados como referente o punto de partida para
                    ejecución del proyecto.
                </p>
                <br />
                <h1>Objetivos del proyecto.</h1>
                <p>
                    Los objetivos son los resultados deseados que se esperan
                    alcanzar con la ejecución de las actividades que integran un
                    proyecto. Deben ser medibles o cuantificables, realistas,
                    limitados en el tiempo, realizables y precisos. Con base en
                    los objetivos se realiza la evaluación de éxito o fracaso
                    del proyecto. Son la ruta o guía de las actividades a
                    realizar, por lo que dan direccionalidad al proyecto.
                    <br />
                    <strong> Objetivo general</strong>
                    Esencia de lo que se espera del proyecto, donde se encierran
                    las metas máximas.
                    <br />
                    <strong>Objetivos específicos</strong>
                    Nivel de detalle mayor y complementario con el general. Pueden
                    ser metas parciales.
                </p>
                <br />
                <h1>Metodología.</h1>
                <p>
                    Breve descripción paso a paso de las actividades que se
                    desarrollaran para dar solución a los objetivos del
                    proyecto. Se debe hacer una diferenciación entre los
                    objetivos y las actividades que ayudan a dar cumplimiento a
                    cada objetivo. Para plantear una metodología se recomienda
                    dividir el proyecto en etapas (objetivos específicos) y
                    hacer una descripción de actividades relacionando los
                    recursos físicos, de personal y de tiempo que son necesarios
                    para llevarlos a cabo. También es importante que por cada
                    actividad se considere un entregable que dé cuenta del
                    cumplimiento de la actividad.
